import { useState, useEffect } from 'react';
import { CONFIG, LINKS } from './config';

const navItems = [
  { label: 'Inicio', href: '#inicio' },
  { label: 'Servicios', href: '#servicios' },
  { label: 'Por qué elegirnos', href: '#por-que-elegirnos' },
  { label: 'Opiniones', href: '#opiniones' },
  { label: 'Preguntas', href: '#faq' },
  { label: 'Contacto', href: '#contacto' },
];

export default function Header() {
  const [isMenuOpen, setIsMenuOpen] = useState(false);
  const [isScrolled, setIsScrolled] = useState(false);

  useEffect(() => {
    const handleScroll = () => {
      setIsScrolled(window.scrollY > 20);
    };
    window.addEventListener('scroll', handleScroll);
    return () => window.removeEventListener('scroll', handleScroll);
  }, []);

  const handleNavClick = (e: React.MouseEvent<HTMLAnchorElement>, href: string) => {
    e.preventDefault();
    const target = document.querySelector(href);
    if (target) {
      target.scrollIntoView({ behavior: 'smooth' });
      setIsMenuOpen(false);
    }
  };

  return (
    <header
      className={`fixed top-0 left-0 right-0 z-50 transition-all duration-300 ${
        isScrolled
          ? 'bg-white/95 backdrop-blur-md shadow-lg'
          : 'bg-transparent'
      }`}
    >
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="flex items-center justify-between h-20 md:h-24 lg:h-28">
          {/* Logo */}
          <a href="#inicio" className="flex items-center gap-2 group">
            <img 
              src="/lubrizen.png" 
              alt="Lubricentro Senzabello"
              className="h-80 md:h-40 lg:h-60 w-auto object-contain transition-transform group-hover:scale-105"
            />
          </a>

          {/* Desktop Navigation */}
          <nav className="hidden lg:flex items-center gap-1">
            {navItems.map((item) => (
              <a
                key={item.href}
                href={item.href}
                onClick={(e) => handleNavClick(e, item.href)}
                className={`px-4 py-2 rounded-lg text-sm font-medium transition-all hover:bg-primary-500/10 ${
                  isScrolled
                    ? 'text-neutral-700 hover:text-primary-700'
                    : 'text-white/90 hover:text-white'
                }`}
              >
                {item.label}
              </a>
            ))}
            <a
              href="https://lubricentro-stock.netlify.app/login"
              target="_blank"
              rel="noopener noreferrer"
              className={`px-4 py-2 rounded-lg text-sm font-medium transition-all hover:bg-primary-500/10 border ${
                isScrolled
                  ? 'text-primary-700 hover:text-primary-800 border-primary-300 hover:border-primary-400'
                  : 'text-white/90 hover:text-white border-white/20 hover:border-white/30'
              }`}
            >
              Login
            </a>
          </nav>

          {/* CTA Buttons */}
          <div className="flex items-center gap-3">
            <a
              href={LINKS.whatsapp}
              target="_blank"
              rel="noopener noreferrer"
              className="hidden md:flex items-center gap-2 bg-gradient-to-r from-green-500 to-green-600 hover:from-green-600 hover:to-green-700 text-white px-5 py-2.5 rounded-full font-semibold text-sm shadow-lg shadow-green-500/30 hover:shadow-green-500/50 transition-all transform hover:scale-105"
            >
              <WhatsAppIcon />
              <span>WhatsApp</span>
            </a>

            {/* Mobile Menu Button */}
            <button
              onClick={() => setIsMenuOpen(!isMenuOpen)}
              className={`lg:hidden p-2 rounded-lg transition-colors ${
                isScrolled
                  ? 'text-neutral-700 hover:bg-neutral-100'
                  : 'text-white hover:bg-white/10'
              }`}
              aria-label="Menú"
            >
              {isMenuOpen ? <CloseIcon /> : <MenuIcon />}
            </button>
          </div>
        </div>
      </div>

      {/* Mobile Menu */}
      <div
        className={`lg:hidden absolute top-full left-0 right-0 bg-white shadow-xl transition-all duration-300 ${
          isMenuOpen
            ? 'opacity-100 translate-y-0'
            : 'opacity-0 -translate-y-4 pointer-events-none'
        }`}
      >
        <nav className="max-w-7xl mx-auto px-4 py-4 space-y-1">
          {navItems.map((item) => (
            <a
              key={item.href}
              href={item.href}
              onClick={(e) => handleNavClick(e, item.href)}
              className="block px-4 py-3 rounded-lg text-neutral-700 hover:bg-primary-50 hover:text-primary-700 font-medium transition-colors"
            >
              {item.label}
            </a>
          ))}
          <a
            href="https://lubricentro-stock.netlify.app/login"
            target="_blank"
            rel="noopener noreferrer"
            className="block px-4 py-3 rounded-lg text-primary-700 hover:bg-primary-50 hover:text-primary-800 font-medium transition-colors border border-primary-200"
          >
            Login
          </a>
          <div className="pt-4 border-t border-neutral-100">
            <a
              href={LINKS.whatsapp}
              target="_blank"
              rel="noopener noreferrer"
              className="flex items-center justify-center gap-2 bg-gradient-to-r from-green-500 to-green-600 text-white px-6 py-3 rounded-full font-semibold shadow-lg"
            >
              <WhatsAppIcon />
              <span>Escribinos por WhatsApp</span>
            </a>
          </div>
        </nav>
      </div>
    </header>
  );
}

